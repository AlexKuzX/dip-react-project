export function GetProviders(value) {

    let array = [];

    providers.forEach((e) => {
        e.types.forEach((el) => {
            if (value === el) {
                array.push(e);
            }
        });
    });
    return array;


};

export function GetProvider(value) {

    let id = undefined;
    providers.forEach(el => {
        if (el.id === value) {
            id = el;
        };
    });
    return id;

};

function isLeapYear(year) {

    return !((year % 4) || (!(year % 100) && (year % 400)));

};

export function GetMonthData(year, month) {

    let result = [];

    monthsOfYear.forEach((item) => {
        if (item.month === month) {

            for (let i = 1; i <= item.days; i++) {
                result.push({ year: year, month: item.month, day: i });

                if (isLeapYear(year) && (item.month === 2) && (i === 28)) {
                    result.push({ year: year, month: item.month, day: 29 });
                };
            };
        };
    });
    return result;

};

function іsWeekend(year, month, day) {

    let date = new Date(year, month - 1, day);
    return ((date.getDay() === 0) || (date.getDay() === 6))
        ? true
        : undefined

};

function isReservedGeneral(id, type, year, month, day) {

    let reserved = [];
    let isReserved = undefined;

    GetProvider(id).reservedDates.forEach(e => {
        reserved.push(e);
    });

    reserved.forEach((el) => {
        if ((year === el.year) && (month === el.month) && (day === el.day) && (isReservedByType(type, el.types) !== true)) {
            isReserved = "reserved";

        } else if ((year === el.year) && (month === el.month) && (day === el.day) && (isReservedByType(type, el.types) === true)) {
            isReserved = "reserveByType";

        };
    });
    return isReserved;


};

function isReservedByType(type, array) {

    let isType = undefined;
    array.forEach((el) => {
        if (el === type) {
            isType = true
        };

    });
    return isType;
}

function isToday(year, month, day) {

    let date = new Date();
    return ((year === date.getFullYear()) && (month === (date.getMonth() + 1)) && (day === date.getDate()))
        ? true
        : false

};

export function PrevDates(year, month, day){

    let currentDate = new Date();
    let checkedDate = new Date(year, month-1, day);
    
    let sCurrentDate = Date.parse(currentDate);
    let sCheckedDate = Date.parse(checkedDate);

    return(sCurrentDate > sCheckedDate)
    ? true
    : false
}

export function DateValue(id, type, year, month, day) {
    if (isToday(year, month, day) === true) {
        return "bg-success"
    } else if (PrevDates(year, month, day) === true) {
        return "cl_prevdays"
    } else if (isReservedGeneral(id, type, year, month, day) === "reserved") {
        return "cl_reserved"
    } else if (isReservedGeneral(id, type, year, month, day) === "reserveByType") {
        return "cl_fullreserved"
    } else if (isToday(year, month, day) === true) {
        return "bg-success"
    } else if (іsWeekend(year, month, day) === true) {
        return "cl_weekday"
    };
};

export function IsFreeDate(id, type, year, month, day) {

    return (isReservedGeneral(id, type, year, month, day) !== "reserveByType")
        ? true
        : false

};

export function CheckIfProvides(id, currentType) {

    let bool = false;
    GetProvider(id).types.forEach((el) => {
        if (el === currentType) {
            bool = true
        }
    });
    return bool;

};

export function GetDayName(year, month, day) {

    let days = ["Su", "Mo", "Tu", "We", "Th", "Fr", "Sa"];
    let date = new Date(year, month - 1, day);
    return days[date.getDay()];

};